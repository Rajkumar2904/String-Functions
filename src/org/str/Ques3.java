package org.str;

public class Ques3 {
public static void main(String[] args) {
	String a="Greens Technology";
	int i = a.indexOf("h");
	System.out.println("Character h in Greens Technology is: "+i);
	String b="SeleniumAutomationTools";
	int j = b.indexOf("o");
	System.out.println("Character o in SeleniumAutomationTool is: "+j);
	String c="Velmurugan";
	int k = c.indexOf("u");
	System.out.println("Character u in Velmurugan is: "+k);
	String d="j a v a p r o g r a m";
	int l = d.indexOf("p");
	System.out.println("Character p in j a v a p r o g r a m is: "+l);
	String e="9095484678";
	int m = e.indexOf("7");
	System.out.println("Character 7 in 9095484678 is: "+m);
}
}
